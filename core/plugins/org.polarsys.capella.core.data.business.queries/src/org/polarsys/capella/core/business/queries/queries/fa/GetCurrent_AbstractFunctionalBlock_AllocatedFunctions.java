package org.polarsys.capella.core.business.queries.queries.fa;

import java.util.ArrayList;
import java.util.List;

import org.eclipse.emf.common.util.EList;
import org.polarsys.capella.common.queries.AbstractQuery;
import org.polarsys.capella.common.queries.queryContext.IQueryContext;
import org.polarsys.capella.core.data.capellacore.CapellaElement;
import org.polarsys.capella.core.data.fa.AbstractFunction;
import org.polarsys.capella.core.data.fa.AbstractFunctionalBlock;
import org.polarsys.capella.core.data.fa.ComponentFunctionalAllocation;

public class GetCurrent_AbstractFunctionalBlock_AllocatedFunctions extends AbstractQuery {

  @SuppressWarnings({ "rawtypes", "unchecked" })
  @Override
  public List<Object> execute(Object input, IQueryContext context) {
    CapellaElement capellaElement = (CapellaElement) input;
    List<CapellaElement> currentElements = getCurrentElements(capellaElement, false);
    return (List) currentElements;
  }

  public List<CapellaElement> getCurrentElements(CapellaElement element, boolean onlyGenerated) {
    List<CapellaElement> currentElements = new ArrayList<CapellaElement>();
    if (element instanceof AbstractFunctionalBlock) {
      EList<ComponentFunctionalAllocation> ownedFunctionalAllocation = ((AbstractFunctionalBlock) element).getOwnedFunctionalAllocation();
      for (ComponentFunctionalAllocation componentFunctionalAllocation : ownedFunctionalAllocation) {
        if (componentFunctionalAllocation.getTargetElement() instanceof AbstractFunction) {
          currentElements.add((CapellaElement) componentFunctionalAllocation.getTargetElement());
        }
      }
    }
    return currentElements;
  }

}
