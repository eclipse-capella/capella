package org.polarsys.capella.core.data.interaction.properties.sections;

import java.util.ArrayList;
import java.util.List;

import org.eclipse.emf.ecore.EObject;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.ui.views.properties.tabbed.TabbedPropertySheetPage;
import org.polarsys.capella.core.data.core.properties.sections.NamedElementSection;
import org.polarsys.capella.core.data.interaction.InteractionPackage;
import org.polarsys.capella.core.data.interaction.properties.Messages;
import org.polarsys.capella.core.ui.properties.controllers.SimpleSemanticFieldController;
import org.polarsys.capella.core.ui.properties.fields.AbstractSemanticField;
import org.polarsys.capella.core.ui.properties.fields.SimpleSemanticField;

/**
 * The StateFragment section.
 */
public class StateFragmentSection extends NamedElementSection {

  private SimpleSemanticField _relatedStateField;
  private SimpleSemanticField _relatedFunctionField;

  /**
   * {@inheritDoc}
   */
  @Override
  public void createContents(Composite parent, TabbedPropertySheetPage aTabbedPropertySheetPage) {
    super.createContents(parent, aTabbedPropertySheetPage);

    boolean displayedInWizard = isDisplayedInWizard();

    _relatedStateField = new SimpleSemanticField(getReferencesGroup(), Messages.getString("StateFragmentSection_RelatedState_Label"), getWidgetFactory(), new SimpleSemanticFieldController()); //$NON-NLS-1$
    _relatedStateField.setDisplayedInWizard(displayedInWizard);

    _relatedFunctionField = new SimpleSemanticField(getReferencesGroup(), Messages.getString("StateFragmentSection_RelatedFunction_Label"), getWidgetFactory(), new SimpleSemanticFieldController()); //$NON-NLS-1$
    _relatedFunctionField.setDisplayedInWizard(displayedInWizard);
  }

  /**
   * {@inheritDoc}
   */
  @Override
  public void loadData(EObject capellaElement) {
    super.loadData(capellaElement);

    _relatedStateField.loadData(capellaElement, InteractionPackage.eINSTANCE.getStateFragment_RelatedAbstractState());
    _relatedFunctionField.loadData(capellaElement, InteractionPackage.eINSTANCE.getStateFragment_RelatedAbstractFunction());
  }

  /**
   * @see org.eclipse.jface.viewers.IFilter#select(java.lang.Object)
   */
  @Override
  public boolean select(Object toTest) {
    EObject eObjectToTest = super.selection(toTest);
    return ((eObjectToTest != null) && (eObjectToTest.eClass() == InteractionPackage.eINSTANCE.getStateFragment()));
  }

  /**
   * {@inheritDoc}
   */
  @Override
  public List<AbstractSemanticField> getSemanticFields() {
    List<AbstractSemanticField> fields = new ArrayList<AbstractSemanticField>();

    fields.addAll(super.getSemanticFields());
    fields.add(_relatedFunctionField);
    fields.add(_relatedStateField);

    return fields;
  }
}
