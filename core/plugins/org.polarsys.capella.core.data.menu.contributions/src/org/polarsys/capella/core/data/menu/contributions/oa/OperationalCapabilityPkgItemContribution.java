package org.polarsys.capella.core.data.menu.contributions.oa;

import org.eclipse.emf.common.command.Command;
import org.eclipse.emf.ecore.EClass;
import org.eclipse.emf.ecore.EStructuralFeature;
import org.eclipse.emf.edit.domain.EditingDomain;

import org.polarsys.capella.common.helpers.EcoreUtil2;
import org.polarsys.capella.core.data.ctx.CtxPackage;
import org.polarsys.capella.core.data.epbs.EpbsPackage;
import org.polarsys.capella.core.data.fa.FaPackage;
import org.polarsys.capella.core.data.la.LaPackage;
import org.polarsys.capella.core.data.oa.OaPackage;
import org.polarsys.capella.core.data.pa.PaPackage;
import org.polarsys.capella.common.data.modellingcore.ModelElement;
import org.polarsys.capella.common.menu.dynamic.contributions.IMDEMenuItemContribution;

public class OperationalCapabilityPkgItemContribution implements IMDEMenuItemContribution {

  /**
   * @see org.polarsys.capella.common.ui.menu.IMDEMenuItemContribution#executionContribution()
   */
  public Command executionContribution(EditingDomain editingDomain, ModelElement containerElement, ModelElement createdElement,
      EStructuralFeature feature) {
    return null;
  }

  /**
   * @see org.polarsys.capella.common.ui.menu.IMDEMenuItemContribution#getMetaclass()
   */
  public EClass getMetaclass() {
    return OaPackage.Literals.OPERATIONAL_CAPABILITY_PKG;
  }

  /**
   * @see org.polarsys.capella.common.ui.menu.IMDEMenuItemContribution#selectionContribution()
   */
  public boolean selectionContribution(ModelElement modelElement, EClass cls, EStructuralFeature feature) {
    return (!EcoreUtil2.isOrIsContainedBy(modelElement, CtxPackage.Literals.SYSTEM_ANALYSIS)
            && !EcoreUtil2.isOrIsContainedBy(modelElement, LaPackage.Literals.LOGICAL_ARCHITECTURE)
            && !EcoreUtil2.isOrIsContainedBy(modelElement, PaPackage.Literals.PHYSICAL_ARCHITECTURE)
            && !EcoreUtil2.isOrIsContainedBy(modelElement, EpbsPackage.Literals.EPBS_ARCHITECTURE) && !EcoreUtil2.isOrIsContainedBy(modelElement,
        FaPackage.Literals.FUNCTION_PKG));
  }
}
